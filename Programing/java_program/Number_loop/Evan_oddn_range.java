package Number_loop;

public class Evan_oddn_range {
	public static void main(String[] args) {
		 System.out.println("Evan Number");
		  for(int i=2;i<=30;i++)
		  {
			  if(i%2==0)
			  {
				  System.out.println(i);
			  }
		  }
		  System.out.println("Odd Number");
		  for(int j =2;j<=30;j++)
		  {
			  if(j%2!=0) {
				  System.out.println(j);
			  }
		  }
		 
	}

}
