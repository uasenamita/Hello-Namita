package Condition;

public class If_condition {
public static void main(String[] args) {

	int n =5;
	if(n==5)  {
		System.out.println("even");
	}
	else if(n==3)   {
		System.out.println("odd");
	}
	else if(n>1)  {
		System.out.println("new");
	}
	else  {
		System.out.println("anything");
	}

}}